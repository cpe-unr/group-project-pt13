#include "metadata.h"

/**
 * @brief 
 * Sets the songname for the WAV file.
 * @param songname 
 * @param newsongname 
 */
void Metadata::setsongname(string songname, string newsongname){
	songname=newsongname;
}
/**
 * @brief 
 * Sets the artist Name for the WAV file.
 * @param artist 
 * @param newartist 
 */
void Metadata::setartist(string artist, string newartist){
	artist=newartist;
}
